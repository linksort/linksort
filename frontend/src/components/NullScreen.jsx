function NullScreenHelper({
  imageSlot,
  headingText,
  detailText,
  callToActionSlot,
}) {
  return (
    <Center flexDirection="column" padding={4}>
      <Box paddingTop={8}>{imageSlot}</Box>
      <Box maxWidth="48ch" textAlign="center">
        <Heading as="h2" size="md" paddingTop={4}>
          {headingText}
        </Heading>
        <Text paddingTop={4}>{detailText}</Text>
        <Box paddingTop={4}>{callToActionSlot}</Box>
      </Box>
    </Center>
  );
}

export default function NullScreen() {
  const {
    areFavoritesShowing,
    pageNumber,
    folderName,
    tagPath,
    handleGoToPrevPage,
    searchQuery,
    handleSearch,
  } = useFilters();

  if (areFavoritesShowing) {
    return (
      <NullScreenHelper
        imageSlot={<Star />}
        headingText="There isn't anything here..."
        detailText="It looks like you don't have any favorites with the current filters applied. Try changing the filters or favoriting an existing link."
      />
    );
  }

  if (folderName.length > 0 && folderName !== "All") {
    return (
      <NullScreenHelper
        imageSlot={<CrystalBall />}
        headingText="This folder is empty."
        detailText="Can you see which links you'll add to this folder in the crystal ball?"
      />
    );
  }

  if (pageNumber > 0) {
    return (
      <NullScreenHelper
        imageSlot={<Beach />}
        headingText="Zilch."
        detailText="There don't seem to be any more links. You might want to go back a page."
        callToActionSlot={<Button onClick={handleGoToPrevPage}>Go back</Button>}
      />
    );
  }

  if (tagPath.length > 0) {
    return (
      <NullScreenHelper
        imageSlot={<Beach />}
        headingText="Nope."
        detailText="There don't seem to be any links here."
      />
    );
  }

  if (searchQuery.length > 0) {
    return (
      <NullScreenHelper
        imageSlot={<ThinkingFace />}
        headingText="Nada."
        detailText="We couldn't find anything."
        callToActionSlot={
          <Button onClick={() => handleSearch("")}>Clear search</Button>
        }
      />
    );
  }

  return (
    <NullScreenHelper
      imageSlot={<WavingHand />}
      headingText="Welcome!"
      detailText="To get started, click the new link button in the upper right to save your first link."
    />
  );
}
